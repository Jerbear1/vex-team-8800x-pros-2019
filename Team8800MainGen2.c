void moveArmOut () {
	if (SensorValue[armPot] > 25) {
		motor[swingingArm] = 115;
	} else if (SensorValue[armPot] <= 25) {
		motor[swingingArm] = 0;
	}
}

void moveArmIn () {
	if (SensorValue[armPot] < 2115) {
		motor[swingingArm] = -125;
	} else if (SensorValue[armPot] >= 2115) {
		motor[swingingArm] = 0;
	}
}

void moveArm (bool forward) {
	if (forward) {
		moveArmOut();
	} else {
		moveArmIn();
	}
}

